/**
 * Склоняет имя в родительный падеж (кого? чего?)
 * Например: "Екатерина" -> "Екатерины", "Иван" -> "Ивана"
 */
export function declineToGenitive(name: string | null | undefined): string {
  if (!name || typeof name !== "string") {
    return "этого человека";
  }

  const trimmed = name.trim();
  if (!trimmed) {
    return "этого человека";
  }

  // Имена, оканчивающиеся на -а (женские и некоторые мужские)
  if (trimmed.endsWith("а")) {
    // Исключения для имен на -а, которые не склоняются
    if (trimmed.endsWith("ия") || trimmed.endsWith("ья")) {
      // Мария -> Марии, Дарья -> Дарьи
      return trimmed.slice(0, -1) + "и";
    }
    // Екатерина -> Екатерины, Анна -> Анны
    return trimmed.slice(0, -1) + "ы";
  }

  // Имена, оканчивающиеся на -я (женские)
  if (trimmed.endsWith("я")) {
    // Исключения
    if (trimmed.endsWith("ия")) {
      // Мария -> Марии (уже обработано выше)
      return trimmed.slice(0, -1) + "и";
    }
    // Наталья -> Натальи, Ольга -> Ольги (но Ольга не на -я, это исключение)
    return trimmed.slice(0, -1) + "и";
  }

  // Имена, оканчивающиеся на -й (мужские)
  if (trimmed.endsWith("й")) {
    // Алексей -> Алексея, Николай -> Николая
    return trimmed.slice(0, -1) + "я";
  }

  // Имена, оканчивающиеся на -ь (мужские и женские)
  if (trimmed.endsWith("ь")) {
    // Игорь -> Игоря, Любовь -> Любови
    // Для женских имен на -ь обычно -и, для мужских -я
    // Упрощенная логика: если имя короткое (<=5 букв), скорее всего женское
    if (trimmed.length <= 5) {
      return trimmed.slice(0, -1) + "и";
    }
    return trimmed.slice(0, -1) + "я";
  }

  // Мужские имена, оканчивающиеся на согласную
  // Иван -> Ивана, Петр -> Петра, Дмитрий -> Дмитрия (уже обработано выше)
  // Для большинства мужских имен просто добавляем -а
  const lastChar = trimmed[trimmed.length - 1];
  if (/[бвгджзклмнпрстфхцчшщ]/.test(lastChar)) {
    return trimmed + "а";
  }

  // Если не подошло ни одно правило, возвращаем как есть
  return trimmed;
}
